"""
Manipulating Strings
"""

# positives[012345678]
text =     'Python s2'
# negatives[987654321]

print(text[2])  # Returns the character from position 2
print(text[-7])  # Returns the character from position -7
print(text[0:6])  # Returns the character from interval 0 to 6 (six not included)
print(text[0:6:2])  # Returns the character from interval in step two

